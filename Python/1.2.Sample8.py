print("Sub of {} and {} == {}".format(a,b,a-b))
print("Prod of {} and {} == {}".format(a,b,a*b))
print("Div of {} and {} == {}".format(a,b,a/b))
print("MOD of {} and {} == {}".format(a,b,a%b))
